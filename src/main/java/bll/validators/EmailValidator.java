package bll.validators;

import java.util.regex.Pattern;

import model.Client;

/**
 * Class that validates the email of a client
 * @author Adrian Baraian
 */
public class EmailValidator implements Validator<Client> {
    /**
     * The pattern for the email validation
     */
    private static final String EMAIL_PATTERN = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])";

    /**
     * Method that validates the email of a client
     * @param c the client to be validated
     * @return true if the email is valid, false otherwise
     */
    public boolean validate(Client c) {
        if(c == null) {
            return false;
        }
        Pattern pattern = Pattern.compile(EMAIL_PATTERN);
        return pattern.matcher(c.getEmail()).matches();
    }

}
